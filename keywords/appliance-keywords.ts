// Generate all possible combinations for SEO
export const generateAllKeywords = () => {
  const allKeywords: string[] = []
  
  Object.values(applianceKeywords).forEach(category => {
    if (category.brands) allKeywords.push(...category.brands)
    if (category.types) allKeywords.push(...category.types)
    if (category.issues) allKeywords.push(...category.issues)
  })
  
  return allKeywords
}

// Get keywords for a specific category
export const getKeywordsForCategory = (category: string) => {
  return applianceKeywords[category as keyof typeof applianceKeywords] || []
}

